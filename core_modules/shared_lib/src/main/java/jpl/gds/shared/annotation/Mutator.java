package jpl.gds.shared.annotation;

import java.lang.annotation.ElementType;
import java.lang.annotation.Retention;
import java.lang.annotation.RetentionPolicy;
import java.lang.annotation.Target;

/**
 * Interface Mutator
 *
 * The Mutator annotation is used to mark methods that modify a class. The presense of this annotation on a
 * method indicates that the class in which it is contained is NOT immutable.
 */
@Target(ElementType.METHOD)
@Retention(RetentionPolicy.RUNTIME)
public @interface Mutator {

}
