/**
 * 
 * This class implements an error detection and correction (EDAC) algorithm.
 * This algorithm is used by some missions to compute the
 * code block error control checksum included in transmitted code blocks.
 * This algorithm returns a 7 bit checsum plus one filler bit in one byte.
 * The filler bit is the rightmost bit of the byte.
 * Much of this class is based on or copied from work done by
 * Tim Reeve and Son Ho in the c language command file tc_fec.c .
 * The specification for this algorithm can be found in
 * the Telecommand Part 1 (Channel Service) Architectural Specification
 * blue book, on pages 3-5 and 3-6.
 *
 */
