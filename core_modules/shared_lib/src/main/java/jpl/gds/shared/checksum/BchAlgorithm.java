	/**
     * Takes the given input and returns a checksum computed
     * over the input using the algorithm embodied by the
     * implementing algorithm.  This algorithm returns a 7 bit
     * checksum over the data, with one padding bit in the rightmost
     * bit of the byte returned.
     *
     *
     * @param data byte[] the data over which the checksum will be
     *                    computed by the algorithm, must be between
     *                    4 and 7 bytes in length inclusive
     *
     * @return byte[] the checksum resulting from the computation over
     *                the input data by the algorithm, 7 bits in
     *                one byte with a padding bit in the rightmost
     *                bit of the byte
     *
     * @exception IllegalArgumentException thrown if the algorithm determines
     *            that the given input data is invalid
     */
    public static byte[] doEncode(final byte[] data) throws IllegalArgumentException
    {
    	//this algorithm returns a 7 bit checksum with a 0 fill bit on the end
    	byte[] checksum = { (byte)0x00 };

    	//ByteBuffers are used throughout to accomodate required bit
    	//and byte manipulation made difficult due to a lack
    	//of "unsigned" data types in java
    	if(data == null)
    	{
    		throw new IllegalArgumentException("EndAroundCarrySumAlgorithm.  Encode(): Null data array passed.");
    	}
    	else if((data.length < 4) || (data.length > 7))
    	{
    		throw new IllegalArgumentException("EndAroundCarrySumAlgorithm.  Encode(): Invalid amount of data passed to be encoded, data contains " +
					       data.length + " bytes.  Data must be between 4 and 7 bytes inclusive.");
    	}


	    byte c = 0;
	    int offset = (7 - data.length) << 8;

	    //need to have a ByteBuffer for the conversion of
	    //the data to an offeset increment - if you just use the
	    //data itself, if it the leftmost bit is 1, it will be
	    //interpreted as a negative number which will screw up
	    //the offset
	    ByteBuffer dataOffsetValue = ByteBuffer.allocate(4);
	    dataOffsetValue.putInt(0);

	    for(int i=0; i<data.length; i++)
	    {
	    	dataOffsetValue.put(3, data[i]);
	    	c = (byte)(c ^ bch_jump_tbl[offset + dataOffsetValue.getInt(0)]);
	    	offset = offset + 256;
	    }

	    checksum[0] = (byte)((byte)~c & (byte)0xfe);

	    return(checksum);
    }

