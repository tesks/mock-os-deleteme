package jpl.gds.dictionary.api.decom.types;

/**
 * Represents information needed to interpret and label a boolean extracted
 * from data via a decom map.
 *
 */
public interface IBooleanDefinition extends INumericDataDefinition {

	/**
	 * @return the string to be used if the extracted boolean value evaluates to true
	 */
	public String getTrueString();

	/**
	 * 
	 * @return the string to be used if the extracted boolean value evaluates to false
	 */
	public String getFalseString();
}
