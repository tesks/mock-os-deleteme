package jpl.gds.dictionary.api.decom.types;

/**
 * Represents any data that may be stored for later
 * reference as a variable in a decom map.
 * 
 *
 */
public interface IStorableDataDefinition extends IChannelizableDataDefinition {
	
	/**
	 * Determine whether data should be usable as a variable. If true,
	 * decom processors should remember the data's name and value and
	 * interpolate later references to the data.
	 * @return true if the data should be stored for later use
	 */
	public boolean shouldStore();

}
