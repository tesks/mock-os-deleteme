package jpl.gds.dictionary.api.eu;

/**
 * The EUType enumeration is used to indicate the type of an Engineering Unit
 * (EU) calculation.
 * <br>
 * <br><b>THIS IS A MULTI-MISSION CORE PUBLIC INTERFACE</b> <br>
 * <br>
 * <b>This is a controlled class. It may not be updated without applicable
 * change requests being filed, and approval of project management. A new
 * version tag must be added below with each revision, and both ECR number and
 * author must be included with the version number.</b> <br><br>
 */
public enum EUType {
	
	/** No EU Type **/
	NONE,

	/** EU calculation is a polynomial expansion **/
	POLYNOMIAL,

	/** EU calculation is a table interpolation **/
	TABLE,

	/**
	 * EU calculation is a simple custom algorithm that implements
	 * IEUCalculation
	 **/
	SIMPLE_ALGORITHM,

	/**
	 * EU calculation is a parameterized custom algorithm that implements
	 * IParameterizedEUCalculation
	 **/
	PARAMETERIZED_ALGORITHM;
}
