package jpl.gds.dictionary.api.decom;

import java.util.List;

/**
 * The IStatementContainer interface is to be extended by all other statement
 * interfaces used by an IDecomMapDefinition object. The statement acts 
 * as a container for other IDecomStatements. 
 * <br>
 * <br><b>THIS IS A MULTI-MISSION CORE PUBLIC INTERFACE</b> <br>
 * <br>
 * <b>This is a controlled class. It may not be updated without applicable
 * change requests being filed, and approval of project management. A new
 * version tag must be added below with each revision, and both ECR number and
 * author must be included with the version number.</b> <br><br>
 * 
 */
public interface IStatementContainer extends IDecomStatement {
	
    /**
     * Adds a decom statement to the container.
     * 
     * @param stmt the IDecomStatement to add 
     */
    public void addStatement(final IDecomStatement stmt);

    /**
     * Gets the decom statements attached to this map.
     * 
     * @return List of Statements; will be an empty list if there are none
     */
    public List<IDecomStatement> getStatementsToExecute();
}
