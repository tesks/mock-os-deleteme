/**
 * OffScope is an enumeration that represents the scope of an alarm off
 * control. <p>
 * <p><b>THIS IS A MULTI-MISSION CORE PUBLIC INTERFACE</b> <p>
 * <p>
 * <b>This is a controlled class. It may not be updated without applicable
 * change requests being filed, and approval of project management. A new
 * version tag must be added below with each revision, and both ECR number and
 * author must be included with the version number.</b> <p><p>
 * 
 * AlarmType is an enumeration of supported telemetry channel alarm types. Every
 * AlarmDefinition object has an associated AlarmType. AlarmDefinitions of the
 * proper type can be created using the AlarmDefinitionFactory.
 * 
 */
public enum OffScope {
	/** Scope is all alarms on a specific channel. */
	CHANNEL, 
	/** Scope is all alarms on a specific channel with a specific level. */
	CHANNEL_AND_LEVEL, 
	/** Scope is a single alarm. */
	ALARM
}