package jpl.gds.dictionary.api.decom.types;

/**
 * Represents a time tag occurrence in generic decom data. Differs
 * from other algorithm invocations because times can define additional attributes..
 *
 */
public interface ITimeDefinition extends IAlgorithmInvocation {
	
	/**
	 * Determine whether this time is a delta-time; i.e., if it should
	 * used as an offset from a previously occurring time tag.
	 * @return true if the time tag is a delta time; false if it is absolute
	 */
	public boolean isDelta();

}
